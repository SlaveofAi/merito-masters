// Define the Provider component
export const LanguageProvider = ({ children }: { children: React.ReactNode }) => {
  const [language, setLanguage] = useState<SupportedLanguage>(() => {
    // Try to get the language from localStorage
    const savedLanguage = localStorage.getItem('language') as SupportedLanguage;
    
    // If there's a saved language, use it; otherwise, try to detect browser language
    if (savedLanguage && ['sk', 'cs', 'en'].includes(savedLanguage)) {
      return savedLanguage;
    }
    
    // Check browser language
    const browserLang = navigator.language.split('-')[0];
    
    // If browser language is supported, use it; otherwise default to Slovak
    if (['sk', 'cs', 'en'].includes(browserLang)) {
      return browserLang as SupportedLanguage;
    }
    
    return 'sk';
  });
  
  // Update localStorage when language changes
  useEffect(() => {
    localStorage.setItem('language', language);
  }, [language]);
  
  // Translation function
  const t = (key: string): string => {
    const translation = translations[language][key];
    return translation || key; // Return the key if no translation found
  };
  
  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

// Custom hook for using the language context
export const useLanguage = () => {
  const context = useContext(LanguageContext);
  
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  
  return context;
};

export default LanguageContext;
